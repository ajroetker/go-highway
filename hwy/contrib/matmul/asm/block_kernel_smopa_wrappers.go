//go:build !noasm && arm64

// SME SMOPA/SUMOPA single-tile kernel wrappers for ARM64.
// SMOPA: signed int8 × signed int8 → int32 outer product.
// SUMOPA: signed int8 × unsigned uint8 → int32 outer product.
package asm

import "unsafe"

// Generate assembly from C using goat
// -march=armv9-a+sme+sme-i16i64 enables SME with integer 8-bit outer products
//go:generate go tool goat ../c/block_kernel_smopa_arm64.c -O3 --target arm64 --target-os darwin -e="-march=armv9-a+sme+sme-i16i64"

// TileSMOPAS8 computes a single 16×16 int32 output tile using signed SMOPA.
//
// aPanel and bPanel must be pre-packed in SMOPA interleaved format:
//
//	panel[k4 * 64 + lane * 4 + g] = value for (tile_lane, k_index = k4*4 + g)
//
// where lane ∈ [0,16), g ∈ [0,4), k4 ∈ [0, kGroups).
//
// c must have length >= 256 (16×16 int32), written with stride 16.
// kGroups is the number of K/4 groups (K must be pre-padded to a multiple of 4).
func TileSMOPAS8(aPanel, bPanel []int8, c []int32, kGroups int) {
	if kGroups == 0 {
		return
	}
	panelSize := kGroups * 64
	if len(aPanel) < panelSize {
		panic("TileSMOPAS8: aPanel too short")
	}
	if len(bPanel) < panelSize {
		panic("TileSMOPAS8: bPanel too short")
	}
	if len(c) < 256 {
		panic("TileSMOPAS8: c slice too short (need 16×16 = 256)")
	}
	tile_smopa_s8(
		unsafe.Pointer(&aPanel[0]),
		unsafe.Pointer(&bPanel[0]),
		unsafe.Pointer(&c[0]),
		int64(kGroups),
	)
}

// TileSUMOPAS8U8 computes a single 16×16 int32 output tile using SUMOPA.
// A panel is signed int8 (activations), B panel is unsigned uint8 (weight quants).
//
// Panel format is the same as TileSMOPAS8/TileUMOPAU8:
//
//	panel[k4 * 64 + lane * 4 + g] = value for (tile_lane, k_index = k4*4 + g)
//
// c must have length >= 256 (16×16 int32), written with stride 16.
// kGroups is the number of K/4 groups (K must be pre-padded to a multiple of 4).
func TileSUMOPAS8U8(aPanel []int8, bPanel []uint8, c []int32, kGroups int) {
	if kGroups == 0 {
		return
	}
	panelSize := kGroups * 64
	if len(aPanel) < panelSize {
		panic("TileSUMOPAS8U8: aPanel too short")
	}
	if len(bPanel) < panelSize {
		panic("TileSUMOPAS8U8: bPanel too short")
	}
	if len(c) < 256 {
		panic("TileSUMOPAS8U8: c slice too short (need 16×16 = 256)")
	}
	tile_sumopa_s8u8(
		unsafe.Pointer(&aPanel[0]),
		unsafe.Pointer(&bPanel[0]),
		unsafe.Pointer(&c[0]),
		int64(kGroups),
	)
}

