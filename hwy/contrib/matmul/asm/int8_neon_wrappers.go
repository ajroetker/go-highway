// ============================================================================
// NEON Fused Int8 Matrix Multiplication
// ============================================================================

// FusedInt8MatMulNEON performs fused Int8 dequantization + matrix multiplication using NEON.
// output[m,n] = sum_k(input[m,k] * (weights[k,n] * scale))
//
// Parameters:
//   - input: [M, K] float32 input matrix (row-major)
//   - weights: [K, N] int8 quantized weights (row-major)
//   - scales: [K, numGroups] float32 per-group scales
//   - output: [M, N] float32 output matrix (row-major, pre-allocated)
//   - m, k, n: matrix dimensions
//   - groupSize: number of columns per scale group
//
// N must be a multiple of 4 (NEON f32 vector width).
func FusedInt8MatMulNEON(input []float32, weights []int8, scales []float32, output []float32, m, k, n, groupSize int) {
	if m == 0 || k == 0 || n == 0 || n%4 != 0 {
		return
	}
	numGroups := (n + groupSize - 1) / groupSize
	if len(input) < m*k || len(weights) < k*n || len(scales) < k*numGroups || len(output) < m*n {
		return
	}
	mVal := int64(m)
	kVal := int64(k)
	nVal := int64(n)
	groupSizeVal := int64(groupSize)
	numGroupsVal := int64(numGroups)
	fused_int8_matmul_neon(
		unsafe.Pointer(&input[0]),
		unsafe.Pointer(&weights[0]),
		unsafe.Pointer(&scales[0]),
		unsafe.Pointer(&output[0]),
		unsafe.Pointer(&mVal),
		unsafe.Pointer(&kVal),
		unsafe.Pointer(&nVal),
		unsafe.Pointer(&groupSizeVal),
		unsafe.Pointer(&numGroupsVal),
	)
}

